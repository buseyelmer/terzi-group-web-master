import { writeFileSync } from "fs";
import { feature } from "topojson-client";
import world from "world-atlas/countries-110m.json" with { type: "json" };

const WIDTH = 960;
const HEIGHT = 480;

function project([lon, lat]) {
  const x = ((lon + 180) / 360) * WIDTH;
  const y = ((90 - lat) / 180) * HEIGHT;
  return [Number(x.toFixed(2)), Number(y.toFixed(2))];
}

function ringToPath(ring) {
  const cmds = ring.map((coord, i) => {
    const [x, y] = project(coord);
    return `${i === 0 ? "M" : "L"}${x} ${y}`;
  });
  return `${cmds.join(" ")} Z`;
}

function geomToPath(geom) {
  if (geom.type === "Polygon") {
    return geom.coordinates.map(ringToPath).join(" ");
  }
  if (geom.type === "MultiPolygon") {
    return geom.coordinates.map((poly) => poly.map(ringToPath).join(" ")).join(" ");
  }
  return "";
}

const countries = feature(world, world.objects.countries);
const landPaths = countries.features
  .map((f) => ({
    id: String(f.id ?? ""),
    d: geomToPath(f.geometry),
  }))
  .filter((entry) => entry.d.length > 0);

const hubs = [
  { id: "paris", name: "Paris", lon: 2.3522, lat: 48.8566 },
  { id: "stuttgart", name: "Stuttgart", lon: 9.1829, lat: 48.7758 },
  { id: "munich", name: "Münih", lon: 11.582, lat: 48.1351 },
  { id: "frankfurt", name: "Frankfurt", lon: 8.6821, lat: 50.1109 },
  { id: "amsterdam", name: "Amsterdam", lon: 4.9041, lat: 52.3676 },
  { id: "istanbul", name: "İstanbul", lon: 28.9784, lat: 41.0082 },
];

function lonLatToXY(lon, lat) {
  const [x, y] = project([lon, lat]);
  return { x, y };
}

const hubPositions = hubs.map((hub) => {
  const { x, y } = lonLatToXY(hub.lon, hub.lat);
  return { ...hub, x, y };
});

const output = `// Auto-generated by scripts/generate-world-map-paths.mjs — do not edit by hand
export const WORLD_MAP_VIEWBOX = { width: ${WIDTH}, height: ${HEIGHT} } as const;

export const WORLD_LAND_PATHS = ${JSON.stringify(landPaths, null, 2)} as const;

export type WorldMapHub = {
  id: string;
  name: string;
  lon: number;
  lat: number;
  x: number;
  y: number;
};

export const WORLD_MAP_HUBS: readonly WorldMapHub[] = ${JSON.stringify(hubPositions, null, 2)} as const;

export function hubToPercent(x: number, y: number) {
  return {
    x: (x / WORLD_MAP_VIEWBOX.width) * 100,
    y: (y / WORLD_MAP_VIEWBOX.height) * 100,
  };
}

export function lonLatToPercent(lon: number, lat: number) {
  const x = ((lon + 180) / 360) * WORLD_MAP_VIEWBOX.width;
  const y = ((90 - lat) / 180) * WORLD_MAP_VIEWBOX.height;
  return hubToPercent(x, y);
}
`;

writeFileSync("feature/home/data/world-map-svg.ts", output);
console.log(`Generated ${landPaths.length} country paths → feature/home/data/world-map-svg.ts`);
